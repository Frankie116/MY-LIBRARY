<esc>           # enters normal mode
<arrow-keys>    # left, right,up,down in text
: <colon>       # to enter cmd line mode
:q              # quits vim without saving
i               # enters insertion mode
:x!             # saves the file
:wq             # also saves the file
: set number    # display line numbers
:2              # jump to line 2
:$              # jump to last line
<shift+4>       # jump to end of line

